package nju.wqy.model;

public class Course {
private int uid;
private String name;
private int tid;
public int getUid() {
	return uid;
}
public void setUid(int uid) {
	this.uid = uid;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getTid() {
	return tid;
}
public void setTid(int tid) {
	this.tid = tid;
}

}
